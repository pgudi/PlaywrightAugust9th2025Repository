// perform slice on Array, It provides eleemnts based on the start and end position
let b = [4,2,6,4,7,5,10,3]
let v1=b.slice(3,5)
console.log(v1);

//splice, it removes elements based on the range

let c = [4,2,6,4,7,5,10,3]
let v2=c.splice(3,5)
console.log(v2);


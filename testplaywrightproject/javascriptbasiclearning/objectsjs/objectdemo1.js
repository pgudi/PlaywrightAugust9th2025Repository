/*
    How to create Objects and 
    How to read values from Object
*/

let student = {
    "firstname":"Santosh",
    "course":"Research and Science",
    "age":22
}

console.log(student.firstname);
console.log(student.course);
console.log(student.age);

console.log(student["firstname"]);
console.log(student["course"]);
console.log(student["age"]);
